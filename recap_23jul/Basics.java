public class Basics {
    public static void main(String[] args) {
        int a = 32;
        int b = 5;
        System.out.println(a + b); // 37
        System.out.println(a * b); // 160
        System.out.println(a / b); // quotient - 6
        System.out.println(a % b); // remainder - 2
        System.out.println(a - b); // 27

        if (a > b) {
            System.out.println(a+" is greater than "+b);
        } else {
            System.out.println(a+" is lesser than "+b);
        }
        // a is greater than b
        // 32 is greater than 5

        
    }
}