public class PrintNums {
    public static void main(String[] args) {
        // Print numbers from 1 to 5 using a for loop.
        
        // initialize
        int num = 1;

        // condition
        while(num <= 5){
            System.out.println(num);
            // increment/decrement
            num = num + 1;
        }
        System.out.println("outside: "+num);



    }
}
